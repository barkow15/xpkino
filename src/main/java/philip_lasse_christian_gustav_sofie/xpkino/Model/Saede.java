package philip_lasse_christian_gustav_sofie.xpkino.Model;

public class Saede {
   private int Raekke;
   private int Saedenummer;



    public Saede(int raekke, int saedenummer){
        Raekke = raekke;
        Saedenummer = saedenummer;

    }
    public int getRaekke() { return Raekke; }

    public void setRaekke(int raekke) { Raekke = raekke; }

    public int getSaedenummer() { return Saedenummer; }

    public void setSaedenummer(int saedenummer) { Saedenummer = saedenummer; }

}
